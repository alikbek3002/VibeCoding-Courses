export const getLanding = (slug: string): Landing | undefined =>
  LANDINGS.find((l) => l.slug === slug);

export function landingMetadata(l: Landing): Metadata {
  const url = `${SITE_URL}/${l.slug}`;
  return {
    title: { absolute: l.metaTitle },
    description: l.description,
    keywords: l.keywords,
    alternates: { canonical: `/${l.slug}` },
    openGraph: {
      type: "article",
      url,
      siteName: "AkylTech",
      locale: "ru_RU",
      title: l.metaTitle,
      description: l.description,
    },
    twitter: {
      card: "summary_large_image",
      title: l.metaTitle,
      description: l.description,
    },
  };
}

export function landingJsonLd(l: Landing) {
  const url = `${SITE_URL}/${l.slug}`;
  return {
    "@context": "https://schema.org",
    "@graph": [
      {
        "@type": "BreadcrumbList",
        itemListElement: [
          { "@type": "ListItem", position: 1, name: "Главная", item: SITE_URL + "/" },
          { "@type": "ListItem", position: 2, name: l.crumb, item: url },
        ],
      },
      {
        "@type": "Service",
        name: l.h1,
        serviceType: l.crumb,
        url,
        provider: {
          "@type": "EducationalOrganization",
          name: "AkylTech",
          url: SITE_URL + "/",
        },
        areaServed: { "@type": "City", name: "Бишкек" },
        description: l.description,
      },
      {
        "@type": "FAQPage",
        mainEntity: l.faq.map((f) => ({
          "@type": "Question",
          name: f.q,
          acceptedAnswer: { "@type": "Answer", text: f.a },
        })),
      },
    ],
  };
}
